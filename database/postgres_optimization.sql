-- ============================================================================
-- Рекомендации по оптимизации PostgreSQL для проекта
-- ============================================================================
-- 
-- Эти настройки нужно применить в postgresql.conf или через ALTER SYSTEM
-- После изменений требуется перезапуск PostgreSQL
--
-- ХАРАКТЕРИСТИКИ СИСТЕМЫ (определены автоматически):
-- - RAM: 3.8GB (доступно: 2.2GB)
-- - CPU: 2 ядра
-- - Диск: HDD (rotational = 1)
-- - PostgreSQL версия: 16.9
--
-- Проверить текущие настройки:
-- SHOW work_mem;
-- SHOW shared_buffers;
-- SHOW effective_cache_size;
-- ============================================================================

-- ============================================================================
-- 1. ПАМЯТЬ ДЛЯ ОПЕРАЦИЙ (work_mem)
-- ============================================================================
-- Увеличивает память для сортировок, хеш-таблиц и других операций
-- Рекомендуется: 64MB - 128MB для систем с ограниченной RAM (3.8GB)
-- ВАЖНО: это память на КАЖДУЮ операцию, не общая!
-- Если у вас много одновременных запросов, не ставьте слишком большое значение
-- Текущее значение: 32MB -> увеличиваем до 64MB
ALTER SYSTEM SET work_mem = '64MB';

-- ============================================================================
-- 2. РАЗДЕЛЯЕМАЯ ПАМЯТЬ (shared_buffers)
-- ============================================================================
-- Кеш PostgreSQL для данных
-- Рекомендуется: 25% от RAM для сервера только с PostgreSQL
-- Если PostgreSQL на том же сервере что и приложение: 15-20% от RAM
-- Для системы с 3.8GB RAM: 20% = ~768MB
-- Текущее значение: 1GB -> уменьшаем до 768MB для освобождения памяти
ALTER SYSTEM SET shared_buffers = '768MB';

-- ============================================================================
-- 3. ЭФФЕКТИВНЫЙ РАЗМЕР КЕША (effective_cache_size)
-- ============================================================================
-- Помогает планировщику запросов оценить доступную память
-- Рекомендуется: 50-75% от RAM
-- Для системы с 3.8GB RAM: 50% = ~2GB
-- Текущее значение: 2GB -> оставляем как есть
ALTER SYSTEM SET effective_cache_size = '2GB';

-- ============================================================================
-- 4. СТОИМОСТЬ СЛУЧАЙНОГО ДОСТУПА (random_page_cost)
-- ============================================================================
-- Для SSD дисков можно уменьшить (по умолчанию 4.0 для HDD)
-- Рекомендуется для SSD: 1.1 - 1.5
-- Для HDD (определено: rotational = 1): оставляем 4.0
-- Текущее значение: 4.0 -> оставляем для HDD
ALTER SYSTEM SET random_page_cost = 4.0;

-- ============================================================================
-- 5. ПАМЯТЬ ДЛЯ ОБСЛУЖИВАНИЯ (maintenance_work_mem)
-- ============================================================================
-- Используется для CREATE INDEX, VACUUM, ALTER TABLE и т.д.
-- Рекомендуется: 512MB - 1GB для систем с ограниченной RAM (3.8GB)
-- Текущее значение: 256MB -> увеличиваем до 512MB
ALTER SYSTEM SET maintenance_work_mem = '512MB';

-- ============================================================================
-- 6. МАКСИМАЛЬНОЕ КОЛИЧЕСТВО СОЕДИНЕНИЙ (max_connections)
-- ============================================================================
-- Убедитесь что не слишком большое (каждое соединение потребляет память)
-- Рекомендуется: 100-200 для средних приложений
-- SHOW max_connections; -- проверить текущее значение

-- ============================================================================
-- 7. ЛОГИРОВАНИЕ МЕДЛЕННЫХ ЗАПРОСОВ
-- ============================================================================
-- Включает логирование запросов дольше указанного времени
ALTER SYSTEM SET log_min_duration_statement = 1000; -- логировать запросы > 1 секунды
ALTER SYSTEM SET log_line_prefix = '%t [%p]: [%l-1] user=%u,db=%d,app=%a,client=%h ';

-- ============================================================================
-- 8. АВТОВАКУУМ И АНАЛИЗ
-- ============================================================================
-- Автоматическая очистка и обновление статистики
ALTER SYSTEM SET autovacuum = on;
ALTER SYSTEM SET autovacuum_max_workers = 3;
ALTER SYSTEM SET autovacuum_naptime = '10s';

-- ============================================================================
-- 9. ПАРАЛЛЕЛЬНЫЕ ЗАПРОСЫ
-- ============================================================================
-- Для больших таблиц можно включить параллельное выполнение
-- Для системы с 2 ядрами CPU: ограничиваем до 2 воркеров
-- Текущее значение: max_parallel_workers_per_gather = 2 (OK), max_parallel_workers = 8 -> уменьшаем до 2
ALTER SYSTEM SET max_parallel_workers_per_gather = 2;
ALTER SYSTEM SET max_parallel_workers = 2;

